function baseUnidade(extra: Partial<Unidade>): Unidade {
  return {
    id: extra.id ?? 0,
    numeroUnidade: extra.numeroUnidade ?? "",
    tipoUnidade: extra.tipoUnidade ?? "apartamento",
    situacao: extra.situacao ?? "vaga",
    unidades: extra.unidades ?? [],
    createdAt: extra.createdAt ?? new Date(),
    updatedAt: extra.updatedAt ?? new Date(),
    ativo: extra.ativo ?? true,
    condominioId: extra.condominioId ?? 1,
    descricao: extra.descricao,
    proprietarioId: extra.proprietarioId,
    metragemM2: extra.metragemM2,
    observacoes: extra.observacoes,
    vagasVeiculos: extra.vagasVeiculos,
  };
}
